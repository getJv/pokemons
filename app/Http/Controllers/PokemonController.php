    {
        if($request->ajax()){

            $p = new Pokemon();
            $p->name= $request->input('name');
            $p->picture= $request->input('picture');
            $p->save();

            return response()->json([
                "mesage"    => "Pokemon criado",
                "pokemon"   => $p  
            ],200);
        }
    }

